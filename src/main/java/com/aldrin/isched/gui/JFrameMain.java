    private void selectSchedule(Long syId) {
        tableModel.setRowCount(0);
        scheduleList = scheduleDAOImpl.selectSchedule(syId);
        tableModel.setRowCount(0);
        for (Schedule s : scheduleList) {
            tableModel.addRow(new Object[]{s.getId(), s.getInstructor().getId(), s.getSubject().getId(), s.getSection().getId(),
                s.getDay().getId(), s.getTime().getId(), s.getRoom().getId(), s.getSchoolYear().getId(),
                s.getInstructor().getInstructor(), s.getSection().getCode(), s.getSubject().getSubject(),
                s.getDay().getDay(), s.getTime().getTime(), s.getRoom().getRoom(), s.getSubject().getUnit()});
        }
    }

