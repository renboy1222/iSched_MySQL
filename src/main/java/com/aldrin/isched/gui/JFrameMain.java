    @Override
    public void mouseClicked(MouseEvent e) {
        if (e.getSource() == jTableClassSchedule) {
            if (e.getButton() == MouseEvent.BUTTON1) {
                if (e.getClickCount() == 1) {
                    int row = jTableClassSchedule.getSelectedRow();
                    if (row != -1) {
//      SCHEDULE.ID,1INSTRUCTOR_ID,2SUBJECT_ID,3SECTION_ID,DAY_ID,TIME_ID,ROOM_ID,SCHOOL_YEAR_ID, 
                        Long scheduleId = Long.parseLong(jTableClassSchedule.getValueAt(row, 0).toString());
                        Long instructorId = Long.parseLong(jTableClassSchedule.getValueAt(row, 1).toString());
                        Long subjectId = Long.parseLong(jTableClassSchedule.getValueAt(row, 2).toString());
                        Long sectionId = Long.parseLong(jTableClassSchedule.getValueAt(row, 3).toString());
                        Long dayId = Long.parseLong(jTableClassSchedule.getValueAt(row, 4).toString());
                        Long timeId = Long.parseLong(jTableClassSchedule.getValueAt(row, 5).toString());
                        Long roomId = Long.parseLong(jTableClassSchedule.getValueAt(row, 6).toString());
                        Long schoolYeaarId = Long.parseLong(jTableClassSchedule.getValueAt(row, 7).toString());
                        Schedule schedule = new Schedule();
                        schedule.setId(scheduleId);//0
                        Instructor instructor = new Instructor();
                        instructor.setId(instructorId);//1
                        Subject subject = new Subject();
                        subject.setId(subjectId);//2
                        Section section = new Section();
                        section.setId(sectionId);//3
                        Day day = new Day();
                        day.setId(dayId);//4
                        Time time = new Time();
                        time.setId(timeId);//5
                        Room room = new Room();
                        room.setId(roomId);//6
                        SchoolYear schoolYear = new SchoolYear();
                        schoolYear.setId(schoolYeaarId);//7
                        schedule.setInstructor(instructor);
                        schedule.setSubject(subject);
                        schedule.setSection(section);
                        schedule.setDay(day);
                        schedule.setTime(time);
                        schedule.setRoom(room);
                        schedule.setSchoolYear(schoolYear);
                        this.schedule = schedule;

                        jButtonUpdate.setEnabled(true);
                        jButtonDelete.setEnabled(true);

                    }
                }
            }
        }
    }

